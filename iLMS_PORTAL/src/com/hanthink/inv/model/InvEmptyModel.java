public class InvEmptyModel extends AbstractModel<String>{

	private static final long serialVersionUID = 364925953219324373L;
	
	private String id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	/** 工厂 */
	private String factoryCode;
	/** 供应商代码 */
	private String supplierNo;
	/** 箱种 */
	private String boxType;
	/** 空容器数量 */
	private String boxQTY;
	/** 创建人 */
	private String creattionUser;
	/** 创建时间 */
	private String creationTime;
	/** 最后修改人 */
	private String lastModifiedUser;
	/** 最后修改时间 */
	private String lastModifiedTime;
	
	public String getFactoryCode() {
		return factoryCode;
	}
	public void setFactoryCode(String factoryCode) {
		this.factoryCode = factoryCode;
	}
	public String getSupplierNo() {
		return supplierNo;
	}
	public void setSupplierNo(String supplierNo) {
		this.supplierNo = supplierNo;
	}
	public String getBoxType() {
		return boxType;
	}
	public void setBoxType(String boxType) {
		this.boxType = boxType;
	}
	public String getBoxQTY() {
		return boxQTY;
	}
	public void setBoxQTY(String boxQTY) {
		this.boxQTY = boxQTY;
	}
	public String getCreattionUser() {
		return creattionUser;
	}
	public void setCreattionUser(String creattionUser) {
		this.creattionUser = creattionUser;
	}
	public String getCreationTime() {
		return creationTime;
	}
	public void setCreationTime(String creationTime) {
		this.creationTime = creationTime;
	}
	public String getLastModifiedUser() {
		return lastModifiedUser;
	}
	public void setLastModifiedUser(String lastModifiedUser) {
		this.lastModifiedUser = lastModifiedUser;
	}
	public String getLastModifiedTime() {
		return lastModifiedTime;
	}
	public void setLastModifiedTime(String lastModifiedTime) {
		this.lastModifiedTime = lastModifiedTime;
	}
	
	@Override
	public String toString() {
		return "InvEmptyModel [id=" + id + ", factoryCode=" + factoryCode + ", supplierNo=" + supplierNo + ", boxType="
				+ boxType + ", boxQTY=" + boxQTY + ", creattionUser=" + creattionUser + ", creationTime=" + creationTime
				+ ", lastModifiedUser=" + lastModifiedUser + ", lastModifiedTime=" + lastModifiedTime + "]";
	}

}
