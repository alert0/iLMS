	public static void checkImport(SwMaterialOrderModel configModel) {
		StringBuffer checkInfo = new StringBuffer();
		String purchaseNo2 = configModel.getPurchaseNo();
		String purchaseRowNo2 = configModel.getPurchaseRowNo();
		String planTime2 = configModel.getPlanTime();//计划交货日期
		String planNum2 = configModel.getPlanNum();//计划交货数
		String orderQty2 = configModel.getOrderQty();//订购数量
		String orderDate2 = configModel.getOrderDate();//订购日期
		String printTime2 = configModel.getPrintTime();//打印时间
		String returnTime2 = configModel.getReturnTime();//反馈时间
		if (StringUtil.isEmpty(purchaseNo2)) {
			checkInfo.append("订单号不能为空;");
		}
		if (StringUtil.isEmpty(purchaseRowNo2)) {
			checkInfo.append("采购单行号不能为空;");
		} else {
			String regex = "^([0-9])*";//非负整数正则
			boolean matches = purchaseRowNo2.matches(regex);
			if (!matches) {
				checkInfo.append("采购单行号为非负整数；");
				configModel.setPurchaseRowNo("");
			}
		}
		if (StringUtil.isEmpty(planTime2)) {
			checkInfo.append("计划交货日期不能为空;");
		} else {
			SimpleDateFormat format = new SimpleDateFormat( "yyyy-MM-dd" );

			try {
				format.parse(planTime2);
			} catch (Exception e) {
				checkInfo.append("计划交货日期格式不正确；");
				configModel.setPrintTime("");
			}
		}
		if (StringUtil.isEmpty(planNum2)) {
			checkInfo.append("计划交货数不能为空;");
		} else {
			String regex = "^([0-9])*";//非负整数正则
			boolean matches = planNum2.matches(regex);
			if (!matches) {
				checkInfo.append("计划交货数为非负整数；");
				configModel.setPlanNum("");
			}
		}
		
		
		if (!StringUtil.isEmpty(orderQty2)) {
			String regex = "^([0-9])*";//非负整数正则
			boolean matches = orderQty2.matches(regex);
			if (!matches) {
				checkInfo.append("订购数若填写请保证为非负整数；");
				configModel.setOrderQty("");
			}
		}
		
		if (!StringUtil.isEmpty(orderDate2)) {
			SimpleDateFormat format = new SimpleDateFormat( "yyyy-MM-dd" );
			try {
				format.parse(orderDate2);
			} catch (Exception e) {
				checkInfo.append("订购日期格式不正确；");
				configModel.setOrderDate("");
			}
		} else {
			configModel.setOrderDate("");
		}
		
		if (!StringUtil.isEmpty(printTime2)) {
			SimpleDateFormat format = new SimpleDateFormat( "yyyy-MM-dd" );
			try {
				format.parse(printTime2);
			} catch (Exception e) {
				checkInfo.append("打印日期格式不正确；");
				configModel.setPrintTime("");
			}
		} else {
			configModel.setPrintTime("");
		}
		
		if (!StringUtil.isEmpty(returnTime2)) {
			SimpleDateFormat format = new SimpleDateFormat( "yyyy-MM-dd" );
			try {
				format.parse(returnTime2);
			} catch (Exception e) {
				checkInfo.append("反馈时间格式不正确；");
				configModel.setReturnTime("");
			}
		} else {
			configModel.setReturnTime("");
		}
		
		if(checkInfo == null || "".equals(checkInfo.toString())){
            configModel.setCheckResult("1");
            configModel.setCheckInfo("");
        }else{
        	configModel.setCheckResult("0");
        	configModel.setCheckInfo(checkInfo.toString());
        }
	}

	

	
	
	
