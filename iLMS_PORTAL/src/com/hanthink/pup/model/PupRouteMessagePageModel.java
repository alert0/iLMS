public class PupRouteMessagePageModel {
	/** 取货区域 */
	private String area;
	/** 卸货地点 */
	private String unloadPlace;
	/** 计算队列 */
	private String unloadPort;
	/** 线路代码 */
	private String routeCode;
	/** 供应商代码 */
	private String supplierNo;
	/** 物流模式 */
	private String pickupType;
	/** 车型 */
	private String carType;
	/** 是否批次取货 */
	private String batch;
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getUnloadPlace() {
		return unloadPlace;
	}
	public void setUnloadPlace(String unloadPlace) {
		this.unloadPlace = unloadPlace;
	}
	public String getUnloadPort() {
		return unloadPort;
	}
	public void setUnloadPort(String unloadPort) {
		this.unloadPort = unloadPort;
	}
	public String getRouteCode() {
		return routeCode;
	}
	public void setRouteCode(String routeCode) {
		this.routeCode = routeCode;
	}
	public String getSupplierNo() {
		return supplierNo;
	}
	public void setSupplierNo(String supplierNo) {
		this.supplierNo = supplierNo;
	}
	public String getPickupType() {
		return pickupType;
	}
	public void setPickupType(String pickupType) {
		this.pickupType = pickupType;
	}
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public String getBatch() {
		return batch;
	}
	public void setBatch(String batch) {
		this.batch = batch;
	}
	@Override
	public String toString() {
		return "PupRouteMessagePageModel [area=" + area + ", unloadPlace=" + unloadPlace + ", unloadPort=" + unloadPort
				+ ", routeCode=" + routeCode + ", supplierNo=" + supplierNo + ", pickupType=" + pickupType
				+ ", carType=" + carType + ", batch=" + batch + "]";
	}
}
