public class PupManualOrderPageModel {
	/** 线路代码 */
	private String routeCode;
	/** 车型 */
	private String carType;
	/** 订单号 */
	private Integer orderNo;
	/** 工作日开始时间 */
	private String workDayStart;
	/** 工作日结束时间 */
	private String workDayEnd;
	
	public String getRouteCode() {
		return routeCode;
	}
	public void setRouteCode(String routeCode) {
		this.routeCode = routeCode;
	}
	public String getCarType() {
		return carType;
	}
	public void setCarType(String carType) {
		this.carType = carType;
	}
	public Integer getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(Integer orderNo) {
		this.orderNo = orderNo;
	}
	public String getWorkDayStart() {
		return workDayStart;
	}
	public void setWorkDayStart(String workDayStart) {
		this.workDayStart = workDayStart;
	}
	public String getWorkDayEnd() {
		return workDayEnd;
	}
	public void setWorkDayEnd(String workDayEnd) {
		this.workDayEnd = workDayEnd;
	}
	@Override
	public String toString() {
		return "PupManualOrderPageModel [routeCode=" + routeCode + ", carType=" + carType + ", orderNo=" + orderNo
				+ ", workDayStart=" + workDayStart + ", workDayEnd=" + workDayEnd + "]";
	}
}
