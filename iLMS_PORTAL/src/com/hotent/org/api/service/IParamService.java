package com.hotent.org.api.service;

/**
 * 参数服务。
 * @author ray
 *
 */
public interface IParamService {
	
	/**
	 * 根据用户ID和指定的KEY获取配置的参数。
	 * @param userId
	 * @param key
	 * @return
	 */
	Object getParamsByKey(String userId,String key);
	
	/**
	 * 根据组织ID和类型获取配置参数。
	 * @param groupId
	 * @param groupType
	 * @return
	 */
	Object getParamByGroup(String groupId,String key);

}
